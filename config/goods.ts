type SellToBuyGoodsDict = { [good_id: string]: string[] };

export const sellToBuyGoodsDict: SellToBuyGoodsDict = Object.entries(buyToSellGoodsDict).reduce((acc, [buyKey, { sell_correspond }]) => {
  sell_correspond.forEach(({ good_id }) => {
    if (!acc[good_id]) {
      acc[good_id] = [];
    }
    acc[good_id].push(buyKey);
  });
  return acc;
}, {} as SellToBuyGoodsDict);

